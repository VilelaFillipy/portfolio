// Helpers
export const getNicho = (slug) => nichos.find((n) => n.slug === slug)

export const getRamo = (ramoSlug) => {
  for (const n of nichos) {
    const r = n.ramos.find((x) => x.slug === ramoSlug)
    if (r) return { ...r, nicho: n }
  }
  return null
}

